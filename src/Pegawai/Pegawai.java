        private void tambahData(){
        String id = txt_idpegawai.getText();
        String nama = txt_namapegawai.getText();
        String alamat = txt_alamat.getText();
        String nohp = txt_no_hp.getText();
        String jenis = cb_pegawai.getSelectedItem().toString();
        String jeniskel = cb_jeniskelamin.getSelectedItem().toString();
        
        //panggil koneksi
        Connection connect = Koneksi.Koneksi.getKoneksi();
        //query untuk memasukan data
        String query = "INSERT INTO `t_pegawai` (id_pegawai, `nama_pegawai`,`jenis_kelamin`,`jenis_pegawai`,`alamat`, `no_hp`) "
                     + "VALUES ('"+id+"', '"+nama+"','"+jeniskel+"','"+jenis+"','"+alamat+"', '"+nohp+"')";
        String query2 = "INSERT INTO `t_users` (id_user, `username`,`password`, `hakakses`) "
                     + "VALUES ('"+id+"', '"+nama+"','"+nama+"', '"+jenis+"')";
        
        try{
            //menyiapkan statement untuk di eksekusi
            PreparedStatement ps = (PreparedStatement) connect.prepareStatement(query);
            ps.executeUpdate(query);
            PreparedStatement ps2 = (PreparedStatement) connect.prepareStatement(query2);
            ps.executeUpdate(query2);
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            
        }catch(SQLException | HeadlessException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan");
            
        }finally{
            tampilData();
            clear();       
        }
    }

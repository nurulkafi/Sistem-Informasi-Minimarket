    private void tampilData(){
        //untuk mengahapus baris setelah input
        int row = tabel_pegawai.getRowCount();
        for(int a = 0 ; a < row ; a++){
            table.removeRow(0);
        }
        
        String query = "SELECT * FROM `t_pegawai` ";
        
        try{
            Connection connect = Koneksi.Koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            while (rslt.next()){
                //menampung data sementara
                   
                    String id = rslt.getString("id_pegawai");
                    String nama = rslt.getString("nama_pegawai");
                    String jeniskel = rslt.getString("jenis_kelamin");
                    String jenis = rslt.getString("jenis_pegawai");
                    String alamat = rslt.getString("alamat");
                    String nohp = rslt.getString("no_hp");
                    
                //masukan semua data kedalam array
                String[] data = {id,nama,jeniskel,jenis,alamat,nohp};
                //menambahakan baris sesuai dengan data yang tersimpan diarray
                table.addRow(data);
            }
                //mengeset nilai yang ditampung agar muncul di table
                tabel_pegawai.setModel(table);
            
        }catch(Exception e){
            System.out.println(e);
        }
       
    }
